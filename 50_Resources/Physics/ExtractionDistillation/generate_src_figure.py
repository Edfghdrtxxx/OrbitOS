    # Save PDF (vector, for publication) -- NO tight_layout()!
    fig.savefig(str(OUT_PDF), bbox_inches="tight", dpi=300)
    print(f"  PDF saved: {OUT_PDF}")

    # Save SVG (vector) -- same figure, no need to recreate
    fig.savefig(str(OUT_SVG), bbox_inches="tight", dpi=300)
    print(f"  SVG saved: {OUT_SVG}")

    plt.close(fig)
    print("Done.")
